    private void cetak_ReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetak_ReportActionPerformed
        // TODO add your handling code here:
        try{
                
            Class.forName(JDBC_DRIVER);
            conn = DriverManager.getConnection(DB_URL,USER,PASS);
            String reportPath = "src/penjualan/barang.jrxml";
            JasperDesign jd =JRXmlLoader.load(reportPath);
            JasperReport jr = JasperCompileManager.compileReport(jd);
            JasperPrint jp = JasperFillManager.fillReport(jr,new HashMap(),conn);
            JasperViewer jv = new JasperViewer(jp,false);
            jv.setVisible(true);
            
         }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_cetak_ReportActionPerformed

